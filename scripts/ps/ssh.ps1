function Get-SshClientInstalled{
    [CmdletBinding(SupportsShouldProcess)]
    Param()
    $State = Get-WindowsCapability -Online | ? Name -like 'OpenSSH.Client*'
    if($Null -ne $State){
        $State = $State.State
        Write-Verbose "OpenSSH.Client State is $State"
        if($State -match 'installed'){
            return $True
        }
    }
    return $False
}

